#!/bin/bash

function parse {
    if [[ $n1 =~ ([0-9]*\.[0-9]*) ]] && [[ $n3 =~ ([0-9]*\.[0-9]*) ]]; then
        n11=`echo ${n1%.*}"0"`
        n12=`echo ${n1#*.}`
        n31=`echo ${n3%.*}"0"`
        n32=`echo ${n3#*.}`
        n10="$(($n11+$n12))"
        n30="$(($n31+$n32))"
    elif [[ $n1 =~ ([0-9]*\.[0-9]*) ]] && [[ $n3 =~ [0-9] ]]; then
        n11=${n1%.*}"0"
        n12=${n1#*.}
        n10="$(($n11+$n12))"
        n30=${n3}"0"
    elif [[ $n1 =~ [0-9] ]] && [[ $n3 =~ ([0-9]*\.[0-9]*) ]]; then
        n10=${n1}"0"
        n31=${n3%.*}"0"
        n32=${n3#*.}
        n30="$(($n31+$n32))"
    else    
        n10=${n1}"0"
        n30=${n3}"0"
    
    fi
}